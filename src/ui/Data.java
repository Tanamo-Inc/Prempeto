    private void but_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_delActionPerformed

        int p = JOptionPane.showConfirmDialog(null, "Do you really want to Delete ?", "Delete", JOptionPane.YES_OPTION);
        if (p == 0) {
            String sql = "delete from Theses where ID=?";
            try {
                ps = conn.prepareStatement(sql);

                ps.setString(1, txt_id.getText());
                ps.execute();

                JOptionPane.showMessageDialog(null, "Deleted");

            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    rs.close();
                    ps.close();
                    // conn.close();
                } catch (Exception e) {
                }
            }

            UpdateJTable();
            close();
            new Theses().show();

        }
    }//GEN-LAST:event_but_delActionPerformed

