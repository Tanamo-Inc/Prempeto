    private void popSet() {
        JPopupMenu menu = new JPopupMenu();
        Action cut = new DefaultEditorKit.CutAction();
        cut.putValue(Action.NAME, "Cut");
        cut.putValue(Action.ACCELERATOR_KEY, KeyStroke.getKeyStroke("control X"));
        menu.add(cut);

        Action copy = new DefaultEditorKit.CopyAction();
        copy.putValue(Action.NAME, "Copy");
        copy.putValue(Action.ACCELERATOR_KEY, KeyStroke.getKeyStroke("control C"));
        menu.add(copy);

        Action paste = new DefaultEditorKit.PasteAction();
        paste.putValue(Action.NAME, "Paste");
        paste.putValue(Action.ACCELERATOR_KEY, KeyStroke.getKeyStroke("control V"));
        menu.add(paste);

        Action sa = new Cor.SelectAll();
        menu.add(sa);

        txt_id.setComponentPopupMenu(menu);
        txt_author.setComponentPopupMenu(menu);
        txt_title.setComponentPopupMenu(menu);
        txt_number.setComponentPopupMenu(menu);
        txt_year.setComponentPopupMenu(menu);
    }

